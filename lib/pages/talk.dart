class MicButton extends StatefulWidget {
  final Function(String) onDifyResponse;
  final Function(bool) onProcessingChange;
  final Function(bool) onListeningChange;

  const MicButton({
    Key? key,
    required this.onDifyResponse,
    required this.onProcessingChange,
    required this.onListeningChange,
  }) : super(key: key);

  @override
  _MicButtonState createState() => _MicButtonState();
}

class _MicButtonState extends State<MicButton>
    with SingleTickerProviderStateMixin {
  double _buttonSize = 80.0;
  final double _maxButtonSize = 90.0;
  late AnimationController _animationController;
  late Animation<double> _sizeAnimation;

  // ÎÖπÏùåÏùÑ ÏúÑÌïú Record Ïù∏Ïä§ÌÑ¥Ïä§
  final AudioRecorder _audioRecorder = AudioRecorder();

  bool _isRecording = false;

  @override
  void initState() {
    super.initState();
    _animationController = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 200),
    );
    _sizeAnimation = Tween<double>(
      begin: _buttonSize,
      end: _maxButtonSize,
    ).animate(_animationController)..addListener(() {
      setState(() {
        _buttonSize = _sizeAnimation.value;
      });
    });
  }

  @override
  void dispose() {
    _animationController.dispose();
    _audioRecorder.dispose();
    super.dispose();
  }

  Future<String> _getRecordingPath() async {
    if (kIsWeb) {
      // ÏõπÏóêÏÑúÎäî ÏûÑÏãú Í≤ΩÎ°ú ÏÇ¨Ïö©
      return 'recording_${DateTime.now().millisecondsSinceEpoch}.wav';
    } else {
      // Î™®Î∞îÏùº/Îç∞Ïä§ÌÅ¨ÌÜ±ÏóêÏÑúÎäî Ïã§Ï†ú Í≤ΩÎ°ú ÏÇ¨Ïö©
      final directory = await getTemporaryDirectory();
      return '${directory.path}/recording_${DateTime.now().millisecondsSinceEpoch}.wav';
    }
  }

  Future<void> _startRecording() async {
    try {
      // ÎßàÏù¥ÌÅ¨ Í∂åÌïú ÌôïÏù∏
      final hasPermission = await _audioRecorder.hasPermission();
      if (!hasPermission) {
        print('Microphone permission denied');
        return;
      }

      final path = await _getRecordingPath();

      setState(() => _isRecording = true);
      widget.onListeningChange(true);
      _animationController.forward();

      print('=== Recording Started ===');
      print('Recording to: $path');

      // ÎÖπÏùå ÏãúÏûë
      await _audioRecorder.start(
        const RecordConfig(
          encoder: AudioEncoder.wav, // .wav ÌòïÏãùÏúºÎ°ú ÎÖπÏùå
          sampleRate: 16000, // 16kHz ÏÉòÌîåÎßÅ Î†àÏù¥Ìä∏
          bitRate: 128000, // 128kbps ÎπÑÌä∏Î†àÏù¥Ìä∏
        ),
        path: path,
      );

      print('Recording started successfully');
    } catch (e) {
      print('Failed to start recording: $e');
      _stopRecording();
    }
  }

  Future<void> _stopRecording() async {
    try {
      setState(() => _isRecording = false);
      widget.onListeningChange(false);
      _animationController.reverse();

      if (await _audioRecorder.isRecording()) {
        final path = await _audioRecorder.stop();
        print('=== Recording Stopped ===');
        print('Recording saved to: $path');

        if (path != null && path.isNotEmpty) {
          // ÎÖπÏùåÎêú ÌååÏùºÏùÑ Dify APIÎ°ú Ï†ÑÏÜ°
          await _processRecordedFile(path);
        }
      }

      print('Recording stopped');
    } catch (e) {
      print('Failed to stop recording: $e');
    }
  }

  Future<void> _processRecordedFile(String filePath) async {
    try {
      print('=== Processing Recorded File ===');
      print('üìÅ File path: $filePath');

      widget.onProcessingChange(true);

      String result;

      if (kIsWeb) {
        // ÏõπÏóêÏÑúÎäî Blob URLÏùÑ Ï≤òÎ¶¨Ìï† Ïàò ÏóÜÏúºÎØÄÎ°ú ÎÖπÏùå Îç∞Ïù¥ÌÑ∞Î•º Î∞îÏù¥Ìä∏Î°ú Í∞ÄÏ†∏ÏôÄÏïº Ìï®      print('Web environment detected');

        // Record Ìå®ÌÇ§ÏßÄÏóêÏÑú Ïõπ ÎÖπÏùå Îç∞Ïù¥ÌÑ∞Î•º Î∞îÏù¥Ìä∏Î°ú Í∞ÄÏ†∏Ïò§Í∏∞ ÏãúÎèÑ
        try {
          final webAudioBytes = await _getWebRecordingBytes();

          if (webAudioBytes != null) {
            result = await DifyAudioService.processAudioFile(
              webAudioBytes,
              'user-123',
            );
          } else {
            result = 'ÏõπÏóêÏÑú Ïò§ÎîîÏò§ Îç∞Ïù¥ÌÑ∞Î•º Í∞ÄÏ†∏Ïò§Îäî Îç∞ Ïã§Ìå®ÌñàÏäµÎãàÎã§.';
          }
        } catch (e) {
          print('Web audio processing error: $e');
          result = 'ÏõπÏóêÏÑú Ïò§ÎîîÏò§ Ï≤òÎ¶¨ Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§: $e';
        }
      } else {
        // ÎÑ§Ïù¥Ìã∞Î∏åÏóêÏÑúÎäî ÌååÏùº Í≤ΩÎ°ú ÏÇ¨Ïö©
        final file = File(filePath);
        if (await file.exists()) {
          final fileSize = await file.length();
          print('‚úÖ File exists, size: ${fileSize} bytes');
        } else {
          print('‚ùå File does not exist!');
          widget.onDifyResponse('ÎÖπÏùå ÌååÏùºÏù¥ ÏÉùÏÑ±ÎêòÏßÄ ÏïäÏïòÏäµÎãàÎã§.');
          return;
        }

        result = await DifyAudioService.processAudioFile(filePath, 'user-123');

        // ÏûÑÏãú ÌååÏùº ÏÇ≠Ï†ú
        if (file.existsSync()) {
          await file.delete();
          print('üóëÔ∏è Temporary file deleted: $filePath');
        }
      }

      print('=== Dify Processing Result ===');
      print('üì§ Result: $result');
      print('================================');

      // ÏΩúÎ∞±ÏùÑ ÌÜµÌï¥ UI ÏóÖÎç∞Ïù¥Ìä∏ Î∞è TTS Ïã§Ìñâ
      widget.onDifyResponse(result);
    } catch (e, stackTrace) {
      print('‚ùå Error processing recorded file: $e');
      print('   Stack trace: $stackTrace');
      widget.onDifyResponse('ÌååÏùº Ï≤òÎ¶¨ Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§: $e');
    } finally {
      widget.onProcessingChange(false);
    }
  }

  // ÏõπÏóêÏÑú ÎÖπÏùåÎêú Îç∞Ïù¥ÌÑ∞Î•º Î∞îÏù¥Ìä∏Î°ú Í∞ÄÏ†∏Ïò§Îäî Ìï®Ïàò
  Future<Uint8List?> _getWebRecordingBytes() async {
    if (!kIsWeb) return null;

    try {
      // ÏõπÏóêÏÑúÎäî Record Ìå®ÌÇ§ÏßÄÍ∞Ä ÎÇ¥Î∂ÄÏ†ÅÏúºÎ°ú MediaRecorder APIÎ•º ÏÇ¨Ïö©Ìï®
      // ÌòÑÏû¨ Record Ìå®ÌÇ§ÏßÄÏùò Ïõπ Íµ¨ÌòÑÏóêÏÑúÎäî ÏßÅÏ†ë Î∞îÏù¥Ìä∏ Îç∞Ïù¥ÌÑ∞Ïóê Ï†ëÍ∑ºÌïòÍ∏∞ Ïñ¥Î†§ÏõÄ
      // ÏûÑÏãú Ìï¥Í≤∞Ï±Ö: ÎçîÎØ∏ WAV Ìó§ÎçîÍ∞Ä Ìè¨Ìï®Îêú Î∞îÏù¥Ìä∏ Îç∞Ïù¥ÌÑ∞ ÏÉùÏÑ±

      print('Generating dummy audio bytes for web test');

      // Í∞ÑÎã®Ìïú WAV Ìó§Îçî + ÎçîÎØ∏ Ïò§ÎîîÏò§ Îç∞Ïù¥ÌÑ∞
      final List<int> wavHeader = [
        // RIFF Ìó§Îçî
        0x52, 0x49, 0x46, 0x46, // "RIFF"
        0x24, 0x08, 0x00, 0x00, // ÌååÏùº ÌÅ¨Í∏∞ - 8
        0x57, 0x41, 0x56, 0x45, // "WAVE"
        // fmt Ï≤≠ÌÅ¨
        0x66, 0x6D, 0x74, 0x20, // "fmt "
        0x10, 0x00, 0x00, 0x00, // fmt Ï≤≠ÌÅ¨ ÌÅ¨Í∏∞
        0x01, 0x00, // Ïò§ÎîîÏò§ Ìè¨Îß∑ (PCM)
        0x01, 0x00, // Ï±ÑÎÑê Ïàò (Î™®ÎÖ∏)
        0x40, 0x1F, 0x00, 0x00, // ÏÉòÌîåÎßÅ Î†àÏù¥Ìä∏ (8000Hz)
        0x80, 0x3E, 0x00, 0x00, // Î∞îÏù¥Ìä∏ Î†àÏù¥Ìä∏
        0x02, 0x00, // Î∏îÎ°ù Ï†ïÎ†¨
        0x10, 0x00, // ÎπÑÌä∏ ÍπäÏù¥ (16ÎπÑÌä∏)
        // data Ï≤≠ÌÅ¨
        0x64, 0x61, 0x74, 0x61, // "data"
        0x00, 0x08, 0x00, 0x00, // Îç∞Ïù¥ÌÑ∞ ÌÅ¨Í∏∞
      ];

      // ÎçîÎØ∏ Ïò§ÎîîÏò§ Îç∞Ïù¥ÌÑ∞ (ÏÇ¨Ïù∏Ìåå)
      final List<int> audioData =
          List.generate(2048, (i) {
            final double t = i / 8000.0;
            final double sample = (32767 * 0.5 * (1 + sin(t * 440 * 2 * pi)));
            final int sampleInt = sample.clamp(-32768, 32767).round();
            return [sampleInt & 0xFF, (sampleInt >> 8) & 0xFF];
          }).expand((x) => x).toList();

      final allBytes = [...wavHeader, ...audioData];
      return Uint8List.fromList(allBytes);
    } catch (e) {
      print('Error getting web recording bytes: $e');
      return null;
    }
  }

  void _onButtonPressed() {
    if (_isRecording) {
      _stopRecording();
    } else {
      _startRecording();
    }
  }

  @override
  Widget build(BuildContext context) {
    return Center(
      child: GestureDetector(
        onTap: _onButtonPressed,
        onTapDown: (_) {
          _animationController.forward();
        },
        onTapUp: (_) {
          _animationController.reverse();
        },
        onTapCancel: () {
          _animationController.reverse();
        },
        child: Container(
          width: _buttonSize,
          height: _buttonSize,
          decoration: BoxDecoration(
            color: _isRecording ? Colors.red.withOpacity(0.8) : Colors.white,
            shape: BoxShape.circle,
            border: Border.all(color: Colors.white, width: 2),
          ),
          child: Icon(
            Icons.mic,
            color: _isRecording ? Colors.white : Colors.black54,
            size: 32,
          ),
        ),
      ),
    );
  }
}

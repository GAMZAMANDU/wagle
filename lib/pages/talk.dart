    if (kIsWeb) {
      print('Web environment: Skipping API connection test due to CORS');
      return;
    }

    print('üîó Testing Dify API connection...');
    final result = await DifyAudioService.testApiKey();
    final isValid = result['valid'] == true;
    if (isValid) {
      print('‚úÖ Dify API connection successful');
    } else {
      print('‚ùå Dify API connection failed: ${result['error']}');
    }
  }

  Future<void> _initTts() async {
    await flutterTts.setLanguage("en-US");
    await flutterTts.setPitch(1.0);
    await flutterTts.setSpeechRate(0.5);

    flutterTts.setStartHandler(() => setState(() => isSpeaking = true));
    flutterTts.setCompletionHandler(() => setState(() => isSpeaking = false));
    flutterTts.setErrorHandler((msg) => setState(() => isSpeaking = false));

    // initStateÏóêÏÑú _speak Ìï®Ïàò Ìò∏Ï∂ú
    WidgetsBinding.instance.addPostFrameCallback((_) {
      _speak();
    });
  }

  Future<void> _speak() async {
    if (isSpeaking) {
      await flutterTts.stop();
      setState(() => isSpeaking = false);
    } else {
      await flutterTts.speak(displayText);
    }
  }

  Future<void> _updateTextAndSpeak(String newText) async {
    setState(() {
      displayText = newText;
      isProcessingDify = false;
    });

    // ÏÉàÎ°úÏö¥ ÌÖçÏä§Ìä∏Î•º TTSÎ°ú ÏùΩÏñ¥Ï£ºÍ∏∞
    await Future.delayed(const Duration(milliseconds: 500)); // ÏïΩÍ∞ÑÏùò ÏßÄÏó∞
    await flutterTts.speak(newText);
  }

  void _startTimer() {
    _timer = Timer.periodic(const Duration(seconds: 1), (timer) {
      setState(() {
        if (_remainingSeconds > 0) {
          _remainingSeconds--;
        } else {
          // 5Î∂ÑÏù¥ ÏßÄÎÇòÎ©¥ ÌôàÏúºÎ°ú Ïù¥Îèô
          _timer?.cancel();
          Navigator.of(context).pop();
        }
      });
    });
  }

  void _stopTimer() {
    _timer?.cancel();
    _timer = null;
  }

  String _formatTime(int seconds) {
    int minutes = seconds ~/ 60;
    int remainingSeconds = seconds % 60;
    return '${minutes.toString().padLeft(2, '0')}:${remainingSeconds.toString().padLeft(2, '0')}';
  }

  @override
  void dispose() {
    flutterTts.stop();
    _stopTimer();
    super.dispose();
  }


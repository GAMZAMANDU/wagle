  bool isSpeaking = false;
  bool isProcessingDify = false;
  bool isListening = false;

  // ÌÉÄÏù¥Î®∏ Í¥ÄÎ†® Î≥ÄÏàòÎì§
  Timer? _timer;
  int _remainingSeconds = 300; // 5Î∂Ñ = 300Ï¥à

  @override
